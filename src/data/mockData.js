// LocalStorage helpers
const KEYS = {
  EVENTS: "reva_events",
  MEMBERS: "reva_members",
  GALLERY: "reva_gallery",
  COMPLAINTS: "reva_complaints",
};

export const initializeData = () => {
  // Force update events in localStorage so hardcoded changes appear immediately
  localStorage.setItem(KEYS.EVENTS, JSON.stringify(events));

  if (!localStorage.getItem(KEYS.MEMBERS)) {
    localStorage.setItem(KEYS.MEMBERS, JSON.stringify(councilMembers));
  }
  if (!localStorage.getItem(KEYS.GALLERY)) {
    localStorage.setItem(KEYS.GALLERY, JSON.stringify(galleryImages));
  }
  if (!localStorage.getItem(KEYS.COMPLAINTS)) {
    localStorage.setItem(KEYS.COMPLAINTS, JSON.stringify([]));
  }
};

export const getData = (key) => {
  try {
    const raw = localStorage.getItem(KEYS[key]);
    return raw ? JSON.parse(raw) : null;
  } catch {
    return null;
  }
};

export const setData = (key, value) => {
  localStorage.setItem(KEYS[key], JSON.stringify(value));
};

export const resetData = () => {
  Object.keys(KEYS).forEach((k) => localStorage.removeItem(KEYS[k]));
};
